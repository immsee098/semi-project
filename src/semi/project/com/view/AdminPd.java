        @Override
        public void mouseClicked(MouseEvent e) {
            int row = table2.getSelectedRow();
            String no = (String) table2.getValueAt(row, 0);
            
            
            try {
                ProductDTO dto = mainDao.PdselectByNo(Integer.parseInt(no));
                tfName.setText(dto.getPdName());
                tfPrice.setText(dto.getPdPrice()+"");
                tfQty.setText(dto.getPdQty()+"");
                taDesc.setText(dto.getPdDesc());
                tfNo.setText(no);
                        
            } catch (SQLException ex) {
                Logger.getLogger(AdminPd.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
