    private void showAllPd() {
        DecimalFormat df = new DecimalFormat("#,###");
        try {
            ArrayList<ProductDTO> list = mainDao.selectPD();
            
            String[][] data = new String[list.size()][3];
            
            for(int i =0; i<list.size(); i++){
                ProductDTO dto = list.get(i);
                String no = dto.getPdNo()+"";
                data[i][0] = no;
                data[i][1] = dto.getPdName();
                String price = df.format(dto.getPdPrice());
                data[i][2] = price;
            }
            model.setDataVector(data, colNames);
            table2.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(AdminPd.class.getName()).log(Level.SEVERE, null, ex);
        }
          
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        if(e.getSource()==btEdit){
            try {
                pdEdit();
            } catch (SQLException ex) {
                Logger.getLogger(AdminPd.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if(e.getSource()==btDel){
            try {
                pdDel();
            } catch (SQLException ex) {
                Logger.getLogger(AdminPd.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (e.getSource()==btAdd){
            try {
                pdInsert();
            } catch (SQLException ex) {
                Logger.getLogger(AdminPd.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void pdEdit() throws SQLException {
        String no = tfNo.getText();
        String name = tfName.getText();
        int price = Integer.parseInt(tfPrice.getText());
        String desc = taDesc.getText();
        int qty = Integer.parseInt(tfQty.getText());

        
        if(no.isEmpty() || no==null){
            JOptionPane.showMessageDialog(this, "수정할 상품을 선택해주세요.");
        } 
        if(name.isEmpty() || name==null) {
            JOptionPane.showMessageDialog(this, "상품명은 공백으로 둘 수 없습니다.");
        }
        if(price<0) {
            JOptionPane.showMessageDialog(this, "금액은 양수로 입력해주세요.");
        }
        
        int no2 = Integer.parseInt(tfNo.getText());
        String img = mainDao.PdselectByNo(no2).getPdImg();
        ProductDTO dto = new ProductDTO();
        dto.setPdNo(no2);
        dto.setPdName(name);
        dto.setPdPrice(price);
        dto.setPdQty(qty);
        dto.setPdImg(img);
        dto.setPdDesc(desc);
        
        
        int cnt = mainDao.updatePd(dto);
        
        if(cnt>0){
            JOptionPane.showMessageDialog(this, "상품 수정 완료");
            showAllPd();
        } else {
            JOptionPane.showMessageDialog(this, "상품 수정 실패");
        }
    }

    private void pdDel() throws SQLException {
        String no = tfNo.getText();
        if(no==null || no.isEmpty()){
            JOptionPane.showMessageDialog(this, "상품을 선택해주세요.");
            return;
        }
        ProductDTO dto = new ProductDTO();
        
        String pdname = tfName.getText();
        int pdprice = Integer.parseInt(tfPrice.getText());
        int pdqty = 0;
        String pddesc = taDesc.getText();
        String pdimg = "테스트";
        
        dto.setPdName(pdname);
        dto.setPdNo(Integer.parseInt(no));
        dto.setPdPrice(pdprice);
        dto.setPdQty(pdqty);
        dto.setPdDesc(pddesc);
        dto.setPdImg(pdimg);
        
        
        int result = JOptionPane.showConfirmDialog(this, "삭제하시겠습니까", "삭제", JOptionPane.YES_NO_OPTION);
         if(result==JOptionPane.YES_OPTION){
             int cnt = mainDao.updatePd(dto);
             
             if(cnt>0){
                 JOptionPane.showMessageDialog(this, "상품 삭제 완료");
                 showAllPd();
             } else{
                 JOptionPane.showMessageDialog(this, "상품 삭제 실패");
             }
         }
        
        
    }

    private void pdInsert() throws SQLException {
        String pdName = tfName.getText();
        int pdPrice = Integer.parseInt(tfPrice.getText());
        int pdQty = Integer.parseInt(tfQty.getText());
        String desc = taDesc.getText();
        
        ProductDTO dto = new ProductDTO();
        dto.setPdName(pdName);
        dto.setPdPrice(pdPrice);
        dto.setPdQty(pdQty);
        dto.setPdDesc(desc);
        
        int cnt = mainDao.addPd(dto);
        
        if(cnt>0){
            JOptionPane.showMessageDialog(this, "상품 등록되었습니다.");
            showAllPd();
        }else{
            JOptionPane.showMessageDialog(this, "상품 등록 실패!");
        }
    }

